    private void quarantinedFilesPanelMouseClicked(java.awt.event.MouseEvent evt) throws IOException, NoSuchAlgorithmException {//GEN-FIRST:event_quarantinedFilesPanelMouseClicked
        changeMainPanelCard("quarantinedCard");

        DefaultTableModel model = (DefaultTableModel) quarantinedTable.getModel();
        model.setRowCount(0);

        MessageDigest md5 = MessageDigest.getInstance("MD5");
        File quarantined_dir = new File("quarantine/");
        SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
        for (File file : quarantined_dir.listFiles()) {
            Object[] attributes = new Object[4];
            String file_name = file.getName();
            String checksum = getFileChecksum(md5, file);
            String quarantine_date = sdf.format(file.lastModified());
            attributes[0] = file_name;
            attributes[1] = quarantine_date;
            attributes[2] = checksum;
            attributes[3] = "Malware";
            model.addRow(attributes);
        }
    }//GEN-LAST:event_quarantinedFilesPanelMouseClicked

    @SuppressWarnings("empty-statement")
    private static String getFileChecksum(MessageDigest digest, File file) throws IOException {
        //Create byte array to read data in chunks
        try ( //Get file input stream for reading the file content
                FileInputStream stream = new FileInputStream(file)) {
            //Create byte array to read data in chunks
            byte[] hashArray = new byte[1024];
            int bytesCount = 0;
            //Read file data and update in message digest
            while ((bytesCount = stream.read(hashArray)) != -1) {
                digest.update(hashArray, 0, bytesCount);
            };
        }

        //Get the hash's bytes
        byte[] bytes = digest.digest();

        //Convert it to hexadecimal format
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < bytes.length; i++) {
            sb.append(Integer.toString((bytes[i] & 0xff) + 0x100, 16).substring(1));
        }

        return sb.toString();
    }

