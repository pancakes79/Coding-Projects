    private void clearReportsButtonMouseClicked(MouseEvent evt) {
        File quarantined_dir = new File("reports/");
        Integer count = 0;
        for (File file : quarantined_dir.listFiles()) {
            if (!file.delete()) {
                JOptionPane.showMessageDialog(null, "Failed to delete " + file.toString(), "", JOptionPane.PLAIN_MESSAGE);
            } else {
                count++;
            }
        }
        JOptionPane.showMessageDialog(null, count.toString() + " reports deleted.", "", JOptionPane.PLAIN_MESSAGE);
    }

