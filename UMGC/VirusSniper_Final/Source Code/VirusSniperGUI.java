    private void scanTimeButtonMouseClicked(MouseEvent evt) {
        try {
            Integer st = Integer.parseInt(scanTimeTF.getText());
            String period = scanTimeCB.getSelectedItem().toString();
            switch (period) {
                case "Days":
                    Settings.setScanTime(st);
                    break;
                case "Months":
                    Settings.setScanTime(st * 31);
                    break;
                case "Years":
                    Settings.setScanTime(st * 365);
                    break;
            }
            JOptionPane.showMessageDialog(null, "Scan time changed to " + st.toString() + " " + period, "", JOptionPane.PLAIN_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error: Scan time must be an integer.", "", JOptionPane.PLAIN_MESSAGE);
        }

    }

